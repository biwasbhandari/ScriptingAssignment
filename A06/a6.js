// In JavaScript, events are actions or occurrences that happen in the web browser, such as clicking a button, pressing a key, or submitting a form. JavaScript allows you to respond to these events by defining event handlers, which are functions that execute when a specific event occurs.
// Events in JavaScript are as follows:

// Click Event:
button.addEventListener("click", function () {});

// KeyDown event:
input.addEventListener("keydown", function (event) {});

// Submit Event:
form.addEventListener("submit", function (event) {
  event.preventDefault();
});

// Load Event:
image.addEventListener("load", function () {});

// Resize Event:
window.addEventListener("resize", function () {});
